package com.flipkart.bean;

/**
 * User bean class
 */
public class User {

	private int userId;
	private String username;

	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
}
